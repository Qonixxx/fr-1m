# 1 (на столбы)

'''
Для обеспечения безопасности на шоссе устанавливаются камеры видеонаблюдения.
Установка возможна только в заранее определённых точках — на мачтах вдоль дороги. Известны расстояния от начала шоссе до каждой мачты.
По нормативам, чтобы камеры не мешали друг другу и не дублировали зоны наблюдения,
любые две установленные камеры должны находиться на расстоянии не менее 10 метров друг от друга.
Необходимо установить максимально возможное количество камер,
не нарушая нормативов и определить максимально возможное расстояние от начала шоссе до ближайшей установленной камеры при этом размещении.

Входные данные
В первой строке — число N   (натуральное, не больше 10000  ) — количество мачт. В следующих N   строках — расстояния от начала шоссе до каждой мачты (натуральные числа не больше 10000  ), каждое в отдельной строке.

Выходные данные
Два целых числа через пробел: максимальное количество установленных камер и максимально возможное расстояние от начала шоссе до ближайшей установленной камеры

Пример входного файла:

8  
15  
25  
5  
35  
12  
45  
20  
3  

При таких входных данных мы можем установить камеры на мачты 45, 35, 25, 15, 5   или 2, 12, 25, 35, 45.
Максимальное количество камер - 5, а максимальное расстояние до ближайшей камеры - 5.
'''


with open("26.txt") as f:
    n = int(f.readline())
    xs = sorted([int(x) for x in f], reverse = True) # для рассмотрения самого худнего сценария для второго вопроса

cams = [xs[0]]
for i in range(1, n):
    if cams[-1] - xs[i] >= 10:
        cams.append(xs[i])
print(len(cams), cams[-1])


