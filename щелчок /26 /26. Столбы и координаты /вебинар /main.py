'''
При проведении эксперимента заряженные частицы попадают на чувствительный экран, представляющий из себя матрицу размером 100 000 на 100 000 точек.
При попадании очередной частицы на экран в файл записываются координаты чувствительного элемента:
номер строки (целое число от 1 до 100 000) и номер позиции в строке (целое число от 1 до 100 000).
Точка экрана, в которую попала хотя бы одна частица, считается светлой, точка, в которую ни одна частица не попала, – тёмной.

Вам нужно определить наибольшую длину цепочки в одной строке, в которой светлые точки идут подряд. Если таких строк несколько, укажите номер первой из подходящих строк.

Входные данные представлены в файле следующим образом. В первой строке входного файла записано целое число N – количество частиц, попавших на экран.
В каждой из следующих N строк записаны по два числа, разделённые пробелом: номер строки и номер позиции в строке.

Запишите в ответе два числа через пробел:
сначала номер строки, в которой находится эта цепочка (если таких строк несколько, запишите минимальный из их номеров),
затем количество светлых точек в самой длинной цепочке из светлых точек.
'''

with open("26.txt") as f:
    n = int(f.readline())
    xs = sorted(set(tuple(map(int, i.split())) for i in f))

l = ans2 = ans1 = 0
    
for i in range(len(xs) - 1):
    if (xs[i][0] == xs[i + 1][0]) and (xs[i + 1][1] - xs[i][1] == 1):
        l += 1
        if l > ans2:
            ans2 = l
            ans1 = xs[i][0]
    else:
        l = 0
print(ans1, ans2 + 1) # ans2 + 1, тк рассматриваются пары 
